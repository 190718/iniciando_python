# Definindo tipo da variavel com "int", para uma operação matematica.
# Definir a variavel como Int para identificação de dados como numerico.
n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))

s1 = n1 + n2

print('A soma dos valores: {} + {} = {}'.format(n1, n2, s1))
